    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        float cels;
        float farh;
        float resultado;

//Celsius a  Farh
    
        if (jRadioButton1.isSelected()){
            cels = Float.parseFloat(jTextField2.getText());
            farh = CelsiusaFarh.convertir(cels);
            jTextField1.setText(String.valueOf(farh));
        }
        else if (jRadioButton2.isSelected()){
            farh = Float.parseFloat(jTextField1.getText());
            cels = FarhaCelsius.convertir(farh);
            jTextField2.setText(String.valueOf(cels));
            
        }
        else {
            jTextField1.setText("");
            jTextField2.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

